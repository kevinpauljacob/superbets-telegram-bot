function removeDuplicates(obj: FallMapType) {
  for (const key1 in obj) {
    for (const key2 in obj[key1]) {
      for (const key3 in obj[key1][key2]) {
        obj[key1][key2][key3] = [...new Set(obj[key1][key2][key3])];
      }
    }
  }
  return obj;
}

export function getRandomFromFallMap(
  width: number,
  line: number,
  input: number,
): number {
  if (
    !fallMap.hasOwnProperty(width) ||
    !fallMap[width].hasOwnProperty(line) ||
    !fallMap[width][line].hasOwnProperty(input)
  )
    return 0;

  const valueArray = fallMap[width][line][input];
  const posX = valueArray[Math.floor(Math.random() * valueArray.length)];

  return posX;
}
