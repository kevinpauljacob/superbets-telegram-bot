function removeDuplicates(obj: FallMapType) {
  for (const key1 in obj) {
    for (const key2 in obj[key1]) {
      for (const key3 in obj[key1][key2]) {
        obj[key1][key2][key3] = [...new Set(obj[key1][key2][key3])];
      }
    }
  }
  return obj;
}

export function getRandomFromFallMap(line: number, input: number): number {
  const posObj = fallMap["750"][line];
  
  if (!posObj.hasOwnProperty(input)) {
    return 0;
  }

  const valueArray = posObj[input];
  const posX = valueArray[Math.floor(Math.random() * valueArray.length)];

  return posX;
}
